package skeleton;

import java.util.*;

/**
 * Enumeration osztály, amely a négy irány közül vehet fel egy értéket.
 */
public class Direction {

    /**
     * Default constructor
     */
    public Direction() {
    }

    /**
     * fel irány
     */
    public void UP;

    /**
     *  jobbra irány
     */
    public void RIGHT;

    /**
     * le irány
     */
    public void DOWN;

    /**
     * bal irány
     */
    public void LEFT;

}